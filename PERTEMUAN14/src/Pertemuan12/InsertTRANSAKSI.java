    private void jButtonTOTALActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTOTALActionPerformed
        try {

            DataRoti r = (DataRoti) jComboBoxNAMAROTI.getSelectedItem();

            if (r == null) {
                JOptionPane.showMessageDialog(this, "Pilih roti terlebih dahulu!");
                return;
            }

            if (textJUMLAHBELI.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Masukkan jumlah beli dulu!");
                return;
            }

            int jumlah = Integer.parseInt(textJUMLAHBELI.getText());

            int total = jumlah * r.getHarga();

            textTOTAL.setText(String.valueOf(total));

            JOptionPane.showMessageDialog(this,
                    "Total harga kamu: Rp " + total);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Jumlah beli harus berupa angka!",
                    "Input Error",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButtonTOTALActionPerformed

