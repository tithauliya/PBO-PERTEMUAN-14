    public String autoID() {
        String id = "T001";

        String sql = "SELECT t.idTransaksi FROM Transaksi t ORDER BY t.idTransaksi DESC";
        List<String> hasil = em.createQuery(sql, String.class).setMaxResults(1).getResultList();

        if (!hasil.isEmpty()) {
            String last = hasil.get(0).substring(1);
            int nomor = Integer.parseInt(last) + 1;
            id = "T" + String.format("%03d", nomor);
        }
        return id;
    }

    private void hitungTotalOtomatis() {
        try {
            DataRoti r = (DataRoti) jComboBoxNAMAROTI.getSelectedItem();
            if (r == null) {
                return;
            }

            if (textJUMLAHBELI.getText().trim().isEmpty()) {
                textTOTAL.setText("");
                return;
            }

            int jumlah = Integer.parseInt(textJUMLAHBELI.getText());
            int total = jumlah * r.getHarga();
            textTOTAL.setText(String.valueOf(total));

        } catch (Exception e) {
            textTOTAL.setText("");
        }
    }

