    private void jButtonSIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSIMPANActionPerformed

        if (textidTRANSAKSI.getText().trim().isEmpty()
                || textTANGGAL.getText().trim().isEmpty()
                || textJUMLAHBELI.getText().trim().isEmpty()
                || textTOTAL.getText().trim().isEmpty()
                || jComboBoxNAMAPELANGGAN.getSelectedItem() == null
                || jComboBoxNAMAROTI.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this,
                    "Total belum dihitung! Mohon isi jumlah atau pilih roti.");
            return;
        }

        int jumlahBeli;
        try {
            jumlahBeli = Integer.parseInt(textJUMLAHBELI.getText().trim());
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Jumlah beli harus berupa angka.");
            textJUMLAHBELI.requestFocus();
            return;
        }

        int totalHarga;
        try {
            totalHarga = Integer.parseInt(textTOTAL.getText().trim());
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Total harga tidak valid.");
            textTOTAL.requestFocus();
            return;
        }

        // Parsing tanggal
        java.sql.Date sqlDate = null;
        String tanggalInput = textTANGGAL.getText().trim();

        DateTimeFormatter[] formatters = new DateTimeFormatter[]{
            DateTimeFormatter.ofPattern("yyyy-MM-dd"),
            DateTimeFormatter.ofPattern("dd-MM-yyyy"),
            DateTimeFormatter.ofPattern("dd/MM/yyyy"),
            DateTimeFormatter.ofPattern("dd MMMM yyyy", new Locale("id", "ID")),
            DateTimeFormatter.ofPattern("dd MMM yyyy", new Locale("id", "ID"))
        };

        java.time.LocalDate parsedLocalDate = null;
        boolean tanggalParsed = false;
        for (DateTimeFormatter fmt : formatters) {
            try {
                parsedLocalDate = java.time.LocalDate.parse(tanggalInput, fmt);
                tanggalParsed = true;
                break;
            } catch (Exception e) {
            }
        }

        if (!tanggalParsed) {
            JOptionPane.showMessageDialog(this,
                    "Format tanggal salah!\nGunakan salah satu format:\n"
                    + "yyyy-MM-dd\n"
                    + "dd-MM-yyyy\n"
                    + "dd/MM/yyyy\n"
                    + "dd MMMM yyyy",
                    "Format Tanggal Salah",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        sqlDate = java.sql.Date.valueOf(parsedLocalDate);

        String idTransaksi = textidTRANSAKSI.getText().trim();
        if (idTransaksi.equals("")) {
            idTransaksi = autoID();
        }

        EntityManager emLocal = null;
        try {
            emLocal = emf.createEntityManager();
            emLocal.getTransaction().begin();

            Transaksi t = new Transaksi();
            t.setIdTransaksi(idTransaksi);
            t.setTanggal(sqlDate);
            t.setJumlahBeli(jumlahBeli);
            t.setTotalHarga(totalHarga);

            Pelanggan p = (Pelanggan) jComboBoxNAMAPELANGGAN.getSelectedItem();
            t.setIdPelanggan(p);

            DataRoti r = (DataRoti) jComboBoxNAMAROTI.getSelectedItem();
            t.setIdRoti(r);

            emLocal.persist(t);
            emLocal.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan!");

            bersih();
            dispose();

        } catch (Exception e) {
            if (emLocal != null && emLocal.getTransaction().isActive()) {
                emLocal.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this,
                    "Error saat menyimpan transaksi: " + e.getMessage(),
                    "Database Error",
                    JOptionPane.ERROR_MESSAGE);
        } finally {
            if (emLocal != null && emLocal.isOpen()) {
                emLocal.close();
            }
        }
    }//GEN-LAST:event_jButtonSIMPANActionPerformed

