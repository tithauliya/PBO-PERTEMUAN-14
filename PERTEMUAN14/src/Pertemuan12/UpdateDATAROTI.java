    private void isicomboboxPELANGGAN() {
        EntityManager em = emf.createEntityManager();
        try {
            TypedQuery<Pelanggan> query = em.createQuery("SELECT p FROM Pelanggan p", Pelanggan.class);

            List<Pelanggan> hasil = query.getResultList();

            if (!hasil.isEmpty()) {
                for (Pelanggan p : hasil) {
                    jComboBoxIDPELANGGAN.addItem(p);
                }
            }

        } finally {
            em.close();
        }
    }

