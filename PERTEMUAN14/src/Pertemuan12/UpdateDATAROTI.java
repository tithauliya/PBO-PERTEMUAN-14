    private void jButtonPERBARUIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPERBARUIActionPerformed
        if (textidroti.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (textnamaroti.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (textharga.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (textstok.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (jComboBoxIDPELANGGAN.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else {
            try {
                EntityManager em = emf.createEntityManager();
                em.getTransaction().begin();

                TypedQuery<DataRoti> query = em.createQuery(
                        "SELECT d FROM DataRoti d WHERE d.idRoti = :idRoti ", DataRoti.class);

                query.setParameter("idRoti", idrotiLama);
                List<DataRoti> hasil = query.getResultList();

                if (!hasil.isEmpty()) {
                    DataRoti roti = hasil.get(0);

                    roti.setIdRoti(textidroti.getText());
                    roti.setNamaRoti(textnamaroti.getText());
                    roti.setHarga(Integer.parseInt(textharga.getText()));
                    roti.setStok(Integer.parseInt(textstok.getText()));

                    Pelanggan p = (Pelanggan) jComboBoxIDPELANGGAN.getSelectedItem();
                    roti.setIdPelanggan(p);

                    em.merge(roti);
                    em.getTransaction().commit();

                    JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Data tidak ditemukan!");
                }

                em.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_jButtonPERBARUIActionPerformed

