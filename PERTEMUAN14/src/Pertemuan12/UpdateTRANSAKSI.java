    private void jButtonPERBARUIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPERBARUIActionPerformed
        try {
            em.getTransaction().begin();

            String idTransaksiBaru = textidTRANSAKSI.getText().trim();
            String tanggalStr = textTANGGAL.getText().trim();
            int jumlahBaru = Integer.parseInt(textJUMLAHBELI.getText().trim());
            int totalBaru = Integer.parseInt(textTOTAL.getText().trim());

            // Konversi String tanggal → Date
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date tanggalDate = sdf.parse(tanggalStr);

            DataRoti rotiBaru = (DataRoti) jComboBoxNAMAROTI.getSelectedItem();
            Pelanggan pelangganBaru = (Pelanggan) jComboBoxNAMAPELANGGAN.getSelectedItem();

            // SET NILAI KE OBJEK TRANSAKSI
            t.setIdTransaksi(idTransaksiBaru);
            t.setTanggal(tanggalDate);     // ✔ SUDAH BENAR (Date)
            t.setJumlahBeli(jumlahBaru);
            t.setTotalHarga(totalBaru);
            t.setIdRoti(rotiBaru);
            t.setIdPelanggan(pelangganBaru);

            em.merge(t);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data transaksi berhasil diperbarui!");
            dispose();

        } catch (Exception e) {
            em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "Gagal memperbarui transaksi: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonPERBARUIActionPerformed

