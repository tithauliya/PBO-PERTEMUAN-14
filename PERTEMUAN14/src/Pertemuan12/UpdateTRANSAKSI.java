    public UpdateTRANSAKSI(
            Frame parent,
            boolean modal,
            String idTransaksi,
            String tanggal,
            String jumlah,
            String idPelanggan,
            String idRoti,
            String total) {

        super(parent, modal);
        initComponents();

        connect();
        isiComboBoxPelanggan();
        isiComboBoxRoti();

        // AMBIL DATA TRANSAKSI DARI DB
        t = em.find(Transaksi.class, idTransaksi);

        if (t == null) {
            JOptionPane.showMessageDialog(this, "Data transaksi tidak ditemukan!");
            dispose();
            return;
        }

        textidTRANSAKSI.setText(t.getIdTransaksi());
        String tanggalFormatted = sdf.format(t.getTanggal());
        textTANGGAL.setText(tanggalFormatted);
        textJUMLAHBELI.setText(String.valueOf(t.getJumlahBeli()));
        textTOTAL.setText(String.valueOf(t.getTotalHarga()));

        jComboBoxNAMAPELANGGAN.setSelectedItem(t.getIdPelanggan());
        jComboBoxNAMAROTI.setSelectedItem(t.getIdRoti());
    }

    private void setSelectedComboRoti(String idRoti) {
        for (int i = 0; i < jComboBoxNAMAROTI.getItemCount(); i++) {
            String r = jComboBoxNAMAROTI.getItemAt(i).toString();
            if (r.equals(idRoti)) {
                jComboBoxNAMAROTI.setSelectedIndex(i);
                return;
            }
        }
    }

    private void setSelectedComboPelanggan(String idPelanggan) {
        for (int i = 0; i < jComboBoxNAMAPELANGGAN.getItemCount(); i++) {
            String p = jComboBoxNAMAPELANGGAN.getItemAt(i).toString();
            if (p.equals(idPelanggan)) {
                jComboBoxNAMAPELANGGAN.setSelectedIndex(i);
                return;
            }
        }
    }

    public void connect() {
        emf = Persistence.createEntityManagerFactory("PERTEMUAN14PU");
        em = emf.createEntityManager();
    }

    public void isiComboBoxPelanggan() {
        List<Pelanggan> listPelanggan = em.createNamedQuery("Pelanggan.findAll", Pelanggan.class).getResultList();
        jComboBoxNAMAPELANGGAN.removeAllItems();
        for (Pelanggan p : listPelanggan) {
            jComboBoxNAMAPELANGGAN.addItem(p);
        }
    }

    public void isiComboBoxRoti() {
        List<DataRoti> listRoti = em.createNamedQuery("DataRoti.findAll", DataRoti.class).getResultList();
        jComboBoxNAMAROTI.removeAllItems();
        for (DataRoti r : listRoti) {
            jComboBoxNAMAROTI.addItem(r);
        }
    }

    public void hitungTotal() {
        try {
            int jumlah = Integer.parseInt(textJUMLAHBELI.getText());
            DataRoti r = (DataRoti) jComboBoxNAMAROTI.getSelectedItem();
            if (r != null) {
                textTOTAL.setText(String.valueOf(jumlah * r.getHarga()));
            }
        } catch (Exception e) {
            textTOTAL.setText("");
        }
    }

    public void bersih() {
        textidTRANSAKSI.setText("");
        textJUMLAHBELI.setText("");
        textTOTAL.setText("");

        jComboBoxNAMAROTI.setSelectedIndex(-1);
        jComboBoxNAMAPELANGGAN.setSelectedIndex(-1);

        textidTRANSAKSI.requestFocus();
    }

